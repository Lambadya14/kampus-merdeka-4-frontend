/* For looping  
  the first argument is to initiate the first initial state
  imagine the second argument is like if
  the last argument is to mondify the index value
*/
for (let index = 0; index < 5; index++) {
  console.log(index);
}

/* While */
let whileIndex = 0;

while (whileIndex < 5) {
  whileIndex++;
  console.log(whileIndex);
}
